class order {
    constructor(orderId,productId) {
        this.orderId = orderId;
        this.productId = productId;
    }

    getOrderId() {
        return this.orderId;
    }

    setOrderId(orderId) {
        this.orderId = orderId;
    }

    getProductId() {
        return this.productId;
    }

    setProductId() {
        this.productId = productId;
    }

}